import 'day_phase.dart';

/// Справочная информация о фазах цикла.
///
/// ПРИНЦИП ФОРМУЛИРОВОК (важно, не менять бездумно):
/// Мы НЕ говорим пользователю «вы почувствуете X» — связь фаз с самочувствием
/// очень индивидуальна, и такие фразы работают как внушение (человек начинает
/// искать в себе симптом). Пишем «некоторые отмечают», «может меняться» —
/// то есть даём информацию о вариантах, а не предсказание её состояния.
///
/// Физиология — по Mihm, Gangooly & Muttukrishna (2011), «The normal menstrual
/// cycle in women», Anim Reprod Sci 124(3–4):229–236.
class PhaseInfo {
  const PhaseInfo({
    required this.title,
    required this.whatHappens,
    required this.someNotice,
  });

  /// Название фазы для заголовка.
  final String title;

  /// Что происходит физиологически — факты, не про самочувствие.
  final String whatHappens;

  /// Что НЕКОТОРЫЕ отмечают. Осторожные формулировки, без обещаний.
  final String someNotice;

  static PhaseInfo? forPhase(DayPhase phase) => _map[phase];

  static const Map<DayPhase, PhaseInfo> _map = {
    DayPhase.menstrual: PhaseInfo(
      title: 'Менструальная фаза',
      whatHappens:
          'Начало цикла. Слизистая оболочка матки обновляется, уровни '
          'эстрогена и прогестерона низкие. Менструация длится в среднем '
          'около 5 дней, но норма у всех своя.',
      someNotice:
          'Некоторые в эти дни отмечают усталость, тянущие ощущения внизу '
          'живота или желание больше отдыхать. У других особых изменений нет — '
          'оба варианта нормальны.',
    ),
    DayPhase.follicular: PhaseInfo(
      title: 'Фолликулярная фаза',
      whatHappens:
          'От начала менструации до овуляции. В яичниках созревают фолликулы, '
          'уровень эстрогена постепенно растёт. Это самая изменчивая по длине '
          'часть цикла — именно она определяет, длиннее или короче будет цикл.',
      someNotice:
          'Некоторые отмечают прибавку энергии и более ровное настроение ближе '
          'к середине фазы. Но это индивидуально и есть далеко не у всех.',
    ),
    DayPhase.fertileWindow: PhaseInfo(
      title: 'Фертильное окно',
      whatHappens:
          'Дни, когда зачатие наиболее вероятно: примерно за 5 дней до овуляции '
          'и в день овуляции. Сперматозоиды могут сохранять способность к '
          'оплодотворению несколько дней, поэтому окно начинается заранее.',
      someNotice:
          'Некоторые замечают изменения выделений — они становятся более '
          'прозрачными и тягучими. Это один из естественных признаков, но '
          'он есть не у всех и не всегда.',
    ),
    DayPhase.ovulation: PhaseInfo(
      title: 'Овуляция',
      whatHappens:
          'Яйцеклетка выходит из фолликула. Момент овуляции сдвигается сильнее, '
          'чем принято думать: он зависит от длины фолликулярной фазы, поэтому '
          'календарный прогноз здесь — оценка, а не точная дата.',
      someNotice:
          'Некоторые отмечают короткое одностороннее покалывание внизу живота '
          'или небольшой подъём базальной температуры на следующий день. '
          'Многие не чувствуют ничего — это тоже норма.',
    ),
    DayPhase.luteal: PhaseInfo(
      title: 'Лютеиновая фаза',
      whatHappens:
          'После овуляции и до следующей менструации. Растёт прогестерон, '
          'базальная температура держится чуть выше. Эта фаза стабильнее '
          'фолликулярной — обычно около 13–15 дней.',
      someNotice:
          'Некоторые в этой фазе отмечают сонливость, изменения аппетита или '
          'чувствительность груди. Выраженность очень разная: у части женщин '
          'заметных изменений нет вовсе.',
    ),
  };

  /// Общая оговорка — показывается под любым описанием фазы.
  static const String disclaimer =
      'Фазы рассчитываются по вашим отметкам и являются оценкой. '
      'Самочувствие в цикле индивидуально: одни и те же дни люди переживают '
      'по-разному, и это нормально. Лея не ставит диагнозов — при вопросах '
      'о здоровье обратитесь к врачу.';
}

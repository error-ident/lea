/// Прогноз овуляции по тестам на ЛГ (лютеинизирующий гормон).
///
/// ЗАЧЕМ. BBT (слой 3) подтверждает овуляцию ПОСТФАКТУМ — подъём виден
/// только после того, как она произошла. ЛГ-тест работает наоборот:
/// положительный результат означает пик LH, а овуляция наступает через
/// 24–36 часов ПОСЛЕ него. Это единственный способ предсказать овуляцию
/// вперёд, а не задним числом.
///
/// ЧТО ЭТО ДАЁТ. Когда в текущем цикле есть положительный тест, прогноз
/// овуляции перестаёт быть календарной оценкой и становится привязанным
/// к измеренному событию. Календарная оценка при этом ОТБРАСЫВАЕТСЯ —
/// измерение всегда сильнее расчёта.
///
/// ОСТОРОЖНОСТЬ. Тест-полоски дают ложноположительные результаты (СПКЯ,
/// некоторые препараты, неверное время суток). Поэтому:
///   • берём ПЕРВЫЙ положительный в цикле — пик LH короткий, повторные
///     положительные обычно тот же пик, а не второй;
///   • игнорируем положительные в начале цикла (до 7-го дня) — там пик
///     физиологически невозможен, это почти наверняка ошибка;
///   • «неясный» результат не считаем положительным.
abstract final class LhAnalysis {
  /// Через сколько дней после пика LH происходит овуляция.
  ///
  /// В литературе — 24–36 часов. Берём 1 день: при дневной точности
  /// календаря дробить смысла нет, а 36 часов чаще округляются к суткам.
  static const int daysFromPeakToOvulation = 1;

  /// Раньше этого дня цикла положительный тест игнорируем.
  ///
  /// Пик LH до 7-го дня физиологически невозможен даже при очень коротком
  /// цикле: фолликулу нужно время созреть. Положительный результат там —
  /// ошибка теста или неверно отмеченный день.
  static const int minPlausibleDay = 7;

  /// Найти день овуляции по положительному ЛГ-тесту.
  ///
  /// [positiveDaysInCycle] — дни цикла (1-й, 2-й…), когда тест был
  /// положительным.
  ///
  /// Возвращает день цикла, в который ожидается овуляция, или null,
  /// если достоверного пика нет.
  static int? ovulationDayFromPositive(List<int> positiveDaysInCycle) {
    final plausible = positiveDaysInCycle
        .where((d) => d >= minPlausibleDay)
        .toList()
      ..sort();
    if (plausible.isEmpty) return null;
    // Первый достоверный положительный = пик LH.
    return plausible.first + daysFromPeakToOvulation;
  }
}
